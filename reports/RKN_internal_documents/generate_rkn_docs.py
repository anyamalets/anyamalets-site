# ---------- Утилиты форматирования ----------
def set_lang_ru(run):
    """Проставляет русский язык в rPr, чтобы Word не подчёркивал русский текст."""
    rpr = run._r.get_or_add_rPr()
    lang = OxmlElement("w:lang")
    lang.set(qn("w:val"), "ru-RU")
    lang.set(qn("w:eastAsia"), "ru-RU")
    lang.set(qn("w:bidi"), "ru-RU")
    rpr.append(lang)


def setup_styles(doc):
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(12)
    rpr = style.element.get_or_add_rPr()
    lang = OxmlElement("w:lang")
    lang.set(qn("w:val"), "ru-RU")
    lang.set(qn("w:eastAsia"), "ru-RU")
    rpr.append(lang)
    for sec in doc.sections:
        sec.left_margin = Cm(2.5)
        sec.right_margin = Cm(1.5)
        sec.top_margin = Cm(2.0)
        sec.bottom_margin = Cm(2.0)


def add_centered_heading(doc, text, level=0):
    h = doc.add_heading(text, level=level)
    h.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for r in h.runs:
        set_lang_ru(r)
    return h


def add_heading(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    for r in h.runs:
        set_lang_ru(r)
    return h


def add_paragraph(doc, text, bold=False, align=None, italic=False):
    p = doc.add_paragraph()
    if align:
        p.alignment = align
    p.paragraph_format.first_line_indent = Cm(1.25)
    r = p.add_run(text)
    r.bold = bold
    r.italic = italic
    set_lang_ru(r)
    return p


def add_bullet(doc, text):
    p = doc.add_paragraph(style="List Bullet")
    r = p.add_run(text)
    set_lang_ru(r)
    return p


def add_table(doc, headers, rows):
    table = doc.add_table(rows=len(rows) + 1, cols=len(headers))
    table.style = "Light Grid Accent 1"
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = ""
        p = cell.paragraphs[0]
        r = p.add_run(h)
        r.bold = True
        set_lang_ru(r)
    for ri, row in enumerate(rows, 1):
        for ci, val in enumerate(row):
            cell = table.rows[ri].cells[ci]
            cell.text = ""
            p = cell.paragraphs[0]
            r = p.add_run(str(val))
            set_lang_ru(r)
    return table


def add_signature_block(doc, role="Оператор"):
    doc.add_paragraph()
    p = doc.add_paragraph()
    p.add_run(f"{role}: ____________________ / {OPERATOR['fio_short']} /")
    set_lang_ru(p.runs[0])
    p2 = doc.add_paragraph()
    p2.add_run(f"Дата: «____» ___________ 20___ г.")
    set_lang_ru(p2.runs[0])


